// Calculate area for given population and density
export const calculateArea = (population, areaPerPerson) => {
  return population * areaPerPerson; // in square meters
};

// Calculate radius for circular overlay (in meters)
export const calculateRadius = (population, areaPerPerson) => {
  const area = calculateArea(population, areaPerPerson);
  return Math.sqrt(area / Math.PI);
};

// Format large numbers for display
export const formatNumber = (num) => {
  if (num >= 1_000_000_000_000_000) {
    return (num / 1_000_000_000_000_000).toFixed(1).replace(/\.0$/, "") + "Q";
  }
  if (num >= 1_000_000_000_000) {
    return (num / 1_000_000_000_000).toFixed(1).replace(/\.0$/, "") + "T";
  }
  if (num >= 1_000_000_000) {
    return (num / 1_000_000_000).toFixed(1).replace(/\.0$/, "") + "B";
  }
  if (num >= 1_000_000) {
    return (num / 1_000_000).toFixed(1).replace(/\.0$/, "") + "M";
  }
  if (num >= 1_000) {
    return (num / 1_000).toFixed(1).replace(/\.0$/, "") + "K";
  }
  return num.toLocaleString();
};

// Format area for display
export const formatArea = (sqMeters) => {
  const sqKm = sqMeters / 1_000_000;
  const sqMiles = sqKm * 0.386102;

  if (sqKm >= 1000) {
    return {
      metric: `${formatNumber(Math.round(sqKm))} km²`,
      imperial: `${formatNumber(Math.round(sqMiles))} mi²`,
    };
  }
  if (sqKm >= 1) {
    return {
      metric: `${sqKm.toFixed(1)} km²`,
      imperial: `${sqMiles.toFixed(1)} mi²`,
    };
  }
  return {
    metric: `${formatNumber(Math.round(sqMeters))} m²`,
    imperial: `${formatNumber(Math.round(sqMeters * 10.764))} ft²`,
  };
};

// Get comparison context for area
export const getAreaComparison = (sqMeters) => {
  const sqKm = sqMeters / 1_000_000;

  const comparisons = [
    { threshold: 0.0001, text: "a small room", area: 0.00001 },
    { threshold: 0.001, text: "a basketball court", area: 0.000437 },
    { threshold: 0.01, text: "a football field", area: 0.00714 },
    { threshold: 0.1, text: "Vatican City", area: 0.44 },
    { threshold: 1, text: "Central Park", area: 3.41 },
    { threshold: 10, text: "Manhattan", area: 59.1 },
    { threshold: 100, text: "Paris", area: 105 },
    { threshold: 500, text: "Los Angeles", area: 1302 },
    { threshold: 5000, text: "Rhode Island", area: 4001 },
    { threshold: 20000, text: "New Jersey", area: 22591 },
    { threshold: 100000, text: "England", area: 130279 },
    { threshold: 500000, text: "California", area: 423970 },
    { threshold: 1000000, text: "Texas", area: 695662 },
    { threshold: 5000000, text: "India", area: 3287263 },
    { threshold: 10000000, text: "USA", area: 9833520 },
    { threshold: 20000000, text: "Russia", area: 17098242 },
  ];

  for (const comp of comparisons) {
    if (sqKm < comp.threshold) {
      const ratio = sqKm / comp.area;
      if (ratio < 0.1) {
        return `smaller than ${comp.text}`;
      } else if (ratio < 0.5) {
        return `about ${Math.round(ratio * 100)}% of ${comp.text}`;
      } else if (ratio < 2) {
        return `roughly the size of ${comp.text}`;
      } else {
        return `about ${ratio.toFixed(1)}x ${comp.text}`;
      }
    }
  }

  return "larger than Russia";
};
